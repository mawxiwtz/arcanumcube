function isSameArrays(arr1: number[], arr2: number[]): boolean {
    if (arr1.length !== arr2.length) return false;

    return !arr1.some((v, i) => v !== arr2[i]);
}

describe('Test for ArcanumCube class', () => {
    test('Test: reset()', () => {
        const rc = new ARCCUBE.ArcanumCube();
        rc.init();
        rc.reset();
        expect(isSameArrays(rc.getStickerColors(), INITIAL_STICKER_COLORS)).toBe(true);
    });

    TwistingList.forEach((i) => {
        test(`Test: twist() - ${i.twist}`, () => {
            const rc = new ARCCUBE.ArcanumCube();
            rc.init();
            rc.twist(i.twist);
            expect(rc.getStickerColors().join('')).toBe(i.result.join(''));
        });
    });

    TwistingList.forEach((i) => {
        test(`Test: getNextStickerColors() - ${i.twist}`, () => {
            const initial = INITIAL_STICKER_COLORS;
            const next = ARCCUBE.getNextStickerColors(initial, i.twist);
            expect(next.join('')).toBe(i.result.join(''));
        });
    });

    TwistingList.forEach((i) => {
        test(`Test: getArrayForTensor() - ${i.twist}`, () => {
            const initial = INITIAL_STICKER_COLORS;
            const next = ARCCUBE.getNextStickerColors(initial, i.twist);
            const tfArray = ARCCUBE.getArrayForTensor(next);
            expect(tfArray.join('')).toBe(i.tensor);
        });
    });

    test(`Test: twist() - multiple twisting`, () => {
        const rc = new ARCCUBE.ArcanumCube();
        rc.init();
        rc.twist([
            'U',
            'U',
            'F',
            'U',
            'U',
            "R'",
            'L',
            'F',
            'F',
            'M',
            'U',
            "F'",
            "B'",
            'R',
            'L',
            'U',
            'U',
            'R',
            'S',
            'U',
            "D'",
            'R',
            "L'",
            'D',
            "R'",
            "L'",
            "E'",
            'D',
            'D',
        ]);
        expect(rc.getStickerColors().join('')).toBe(
            '040255010424444414505031535303225313121514131202305232',
        );
    });

    TwistingList.forEach((i) => {
        const m = getCubePermutationGroup(i.twist);
        const [bef, aft] = [m.stickers.before, m.stickers.after];
        test(`Test: getCubePermutationGroup() - ${i.twist}`, () => {
            const data = StickerPermutationGroupMap[i.twist];
            expect(isSameArrays(bef.sort(), data[0].sort())).toBe(true);
            expect(isSameArrays(aft.sort(), data[1].sort())).toBe(true);
        });
    });

    TwistingList.forEach((i) => {
        function getNextStickers(
            stickers: ARCCUBE.StickerColor[],
            twist: Twist,
        ): ARCCUBE.StickerColor[] {
            const result = [...stickers]; // clone
            const m = getCubePermutationGroup(twist);
            const [bef, aft] = [m.stickers.before, m.stickers.after];
            for (let i = 0; i < bef.length; i++) {
                result[aft[i]] = stickers[bef[i]];
            }
            return result;
        }

        test(`Test: getNextStickers() - ${i.twist}`, () => {
            const initial = INITIAL_STICKER_COLORS;
            const next = getNextStickers(initial, i.twist);
            expect(next.join('')).toBe(i.result.join(''));
        });
    });
});
